
//1.example. The following code uses a for loop to print all the elements of the array fruits:

let fruits = ["Apple", "Orange", "Banana"];
for (let i = 0; i < fruits.length; i++) {
    console.log(fruits[i]);
}

//In the above code, the for loop starts at index 0 and keeps incrementing the index until it reaches the end of the array.
// The length property of the array returns the number of elements in the array. 
//The for loop will stop when the index is equal to the length of the array


