//The forEach() method executes a provided function once for each array element. For example,

let fruits = ["Apple", "Orange", "Banana"];
fruits.forEach((i) => {
    console.log(i);
});

//In the above code, the forEach() method takes a callback function as an argument. 
//The callback function takes the current element as an argument. 
//The forEach() method will call the callback function once for each element in the array.