/*
//setTimeout is used to execute a function after a specified amount of time has passed. 
//The syntax for setTimeout is as follows:

setTimeout(function, delay);

*/


//1.The function parameter is the function that you want to execute.
//2.The delay parameter is the number of milliseconds to wait before executing the function.


 //Here's an example that demonstrates how to use setTimeout:

setTimeout(function() {
  alert('Hello, world!');
}, 1000);

//This code will wait for 1000 milliseconds (1 second) and then display an alert with the message "Hello, world!".
