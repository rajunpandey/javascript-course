//JavaScript allows you to check the data type by using the typeof operator

let x = 5;
console.log(typeof x) //  'number'

console.log(typeof "rajun") // 'string'

console.log(typeof true) // 'boolean'

//note The typeof operator returns the type of the data as a string. The 'number' type represents both integer and float types, the string and boolean represent their respective types.