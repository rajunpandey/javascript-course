//The delete() method removes the element at the specified index from an array. For example, 

let fruits = ["Apple", "Orange", "Banana"];
delete fruits[1];
console.log(fruits); 


//Note that the delete() method does not change the length of the array.
// It simply removes the element at the specified index and replaces it with an empty item.
// Also, the delete() method does not return the removed element like the pop() method.